    private void validarBusqueda() throws HeadlessException {
        if(!"Ingrese c贸digo del Curso".equals(txtBusqueda.getText())){
            if(ListaCursos.size()==0){
                JOptionPane.showMessageDialog(null, "Lista de Cursos Vacia",
                        "Buscar Curso", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                recorrerlistaBuscar();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingresa el codigo!",
                    "Buscar Curso", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    private void recorrerlistaBuscar() throws HeadlessException {
        String codigo = txtBusqueda.getText();
        boolean encontrado = false;
        for (int i = 0; i < ListaCursos.size(); i++) {
            if (ListaCursos.get(i).getCodigo().equals(codigo)) {
                DefaultListModel model =new DefaultListModel();
                listCursos.setModel(model);
                model.removeAllElements();
                model.addElement(ListaCursos.get(i).toString());  
                encontrado=true;
            } 
        }
        if(encontrado==false){
            JOptionPane.showMessageDialog(null, "Curso no encontrado",
                    "Buscar Curso", JOptionPane.INFORMATION_MESSAGE);
            mostrarLista();
        }
    }
    private void placeholder(){
        this.txtBusqueda.setForeground(Color.GRAY);
        this.txtBusqueda.setText("Ingrese c贸digo del Curso");
        this.txtBusqueda.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (txtBusqueda.getText().equals("Ingrese c贸digo del Curso")) {
                    txtBusqueda.setText("");
                    txtBusqueda.setForeground(Color.BLACK);
                }
            }
            @Override
            public void focusLost(FocusEvent e) {
                if (txtBusqueda.getText().isEmpty()) {
                    txtBusqueda.setForeground(Color.GRAY);
                    txtBusqueda.setText("Ingrese c贸digo del Curso");
                }
            }
        });
    }
    private void mostrarLista(){
        DefaultListModel model =new DefaultListModel();        
        listCursos.setModel(model); 
        model.removeAllElements();
        for (int i = 0; i < ListaCursos.size(); i++) {
            model.addElement(ListaCursos.get(i).toString());
        }
    }
