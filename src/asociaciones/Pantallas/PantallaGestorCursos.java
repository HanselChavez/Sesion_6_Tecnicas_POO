    private void btnEliminarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCursoActionPerformed
       
        int k = listCursos.getSelectedIndex();
        if(k!=-1){         
            String  j = listCursos.getSelectedValue().trim();  
            for (int i = 0; i < ListaCursos.size(); i++) {
                if(ListaCursos.get(i).toString().equals(j)){ 
                    int respuesta = JOptionPane.showConfirmDialog(null,
                    "Curso Seleccionado: \n"+ListaCursos.get(i).getNombre()
                    +"\nEstas seguro de Eliminar?",
                    "Eliminar Curso", JOptionPane.YES_NO_OPTION);
                    estadoEliminado(respuesta, i);                   
                }
            } 
        }
        else{
            JOptionPane.showMessageDialog(null,"Debe seleccionar un curso"
            + " de la lista","Eliminar Curso",
            JOptionPane.INFORMATION_MESSAGE);
        }              
    }//GEN-LAST:event_btnEliminarCursoActionPerformed

    private void estadoEliminado(int respuesta, int i) throws HeadlessException {
        if (respuesta == JOptionPane.YES_OPTION) {
            ListaCursos.remove(i);
            mostrarLista();
            JOptionPane.showMessageDialog(null,"Curso Eliminado"
                    ,"Eliminar Curso",JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null,"Curso no Eliminado"
                    ,"Eliminar Curso",JOptionPane.INFORMATION_MESSAGE);
        }
    }
