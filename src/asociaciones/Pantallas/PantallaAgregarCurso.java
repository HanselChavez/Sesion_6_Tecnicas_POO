    private void limpiarControles() {
        txtCodigoC.setText("");
        txtCreditosC.setText("");
        txtNombreC.setText("");
    }
    private void abrirGestorCursos() {
        PantallaGestorCursos pantallaGestorCursos = new
            PantallaGestorCursos(PantallaGestorCursos.getListaCursos());
        pantallaGestorCursos.setVisible(true);
    }    
    private void datosCurso() throws NumberFormatException {
        String codigo = txtCodigoC.getText();
        Integer credito = Integer.valueOf(txtCreditosC.getText());
        String nombre = txtNombreC.getText();
        curso = new Curso(codigo, credito, nombre);
        List<Curso> listaCurso = PantallaGestorCursos.getListaCursos();
        listaCurso.add(curso);
        PantallaGestorCursos.setListaCursos(listaCurso);
    }

