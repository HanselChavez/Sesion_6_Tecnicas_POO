    private void btnGuardarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCursoActionPerformed
        // TODO add your handling code here:
        if( !(txtCodigoC.getText().isEmpty() ||
            txtCreditosC.getText().isEmpty() || 
            txtNombreC.getText().isEmpty())){             
            datosCurso();  
            limpiarControles();
            JOptionPane.showMessageDialog(null, "Curso Agregado!",
                "Guardar Curso", JOptionPane.INFORMATION_MESSAGE);            
        }else{
            JOptionPane.showMessageDialog(null, "Completa todos los campos!",
                "Guardar Curso", JOptionPane.INFORMATION_MESSAGE);
        }       
    }//GEN-LAST:event_btnGuardarCursoActionPerformed
